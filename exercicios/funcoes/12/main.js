let contador = 0; // Variável global que guarda o valor do contador

function incrementaContador() { // Função que aumenta o contador
  contador++; // Incrementa em 1
  console.log("Contador:", contador); // Exibe no console
}

// Chamadas de exemplo para testar
incrementaContador(); // Exibe 1
incrementaContador(); // Exibe 2
incrementaContador(); // Exibe 3
